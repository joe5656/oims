        String employeepw = this.jTextField2.getText().trim();
        
        if(employeeId.equals("") || employeepw.equals(""))
        {
            return;
        }
        if(this.itsEm_.authEmployee(employeeId, employeepw))
        {
            this.itsTx_.userAuth(this.itsEm_.getEmployee(employeeId));
            this.dispose();
        }
        else
        {
            this.itsTx_.userUnauth();
        }
        
